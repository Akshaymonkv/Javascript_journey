//Example for primitive data types in Javascript.
//NB: To run a js file, the command used is node file_name.js
/*Primitive datatypes*/
      //1. num (numeric data type)
      var x = 7
      console.log(x);
    
      //2. string (collection of characters)
      var veg = 'Cabbage'
      console.log(veg);

      //3. boolean (returns true or false)
      var x = 5
      var y = 6
      var z = x>y
      console.log(z);

      //4. null (as the name suggests a null value. Mostly for initialization)
      var n = null
      console.log(n);

      //5. undefined (undefined is when there is no value assigned)
      var name
      console.log(name);

